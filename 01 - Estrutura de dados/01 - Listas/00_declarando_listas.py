frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = [] # forma mais comum de declarar uma lista, entre colchetes
print(frutas)

letras = list("python") # declarando usando o construtor (palavra reservada list) cada letra é um valor
print(letras)

numeros = list(range(10)) # declarando usando a função range (irá gerar valores de 0-9 neste exemplo)
print(numeros)

carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True] # a lista aceita vários tipos de valores/objs
print(carro)

